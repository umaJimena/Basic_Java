public class ejercicio3 {
    //Diseña un programa que imprima una cuenta atrás desde 10 a 0.
    public static void main(String[] args) {
        int i = 10; //Como es una cuenta regresiva inicializamos con el 10
        while(i != -1){//Vamos a ir disminuyendo -1, mientras que i sea distinto de -1 
            System.out.println( i );
            i--;
        }

    }
}
